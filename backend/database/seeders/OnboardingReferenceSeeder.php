    public function run(): void
    {
        foreach (self::VOCATIONS as $vocationName) {
            Vocation::query()->firstOrCreate(['name' => $vocationName]);
        }

        foreach (self::RAW_SCHOOLS as $entry) {
            $idx = strrpos($entry, ' - ');
            if ($idx === false) {
                continue;
            }

            $schoolName = trim(substr($entry, 0, $idx));
            $cityName = trim(substr($entry, $idx + 3));

            if ($schoolName === '' || $cityName === '') {
                continue;
            }

            $city = City::query()->firstOrCreate(['name' => $cityName]);

            School::query()->firstOrCreate(
                ['city_id' => $city->id, 'name' => $schoolName],
                ['city_id' => $city->id, 'name' => $schoolName],
            );
        }
    }
