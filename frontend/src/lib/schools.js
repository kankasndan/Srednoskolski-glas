// Group into [{ city, schools: [...] }] in order of first appearance.
// City is the text after the LAST " - " so school names with hyphens still work.
export const CITIES = (() => {
  const map = new Map();
  for (const entry of RAW_SCHOOLS) {
    const idx = entry.lastIndexOf(" - ");
    const city = idx === -1 ? entry : entry.slice(idx + 3).trim();
    const school = idx === -1 ? null : entry.slice(0, idx).trim();
    if (!map.has(city)) map.set(city, []);
    if (school && !map.get(city).includes(school)) map.get(city).push(school);
  }
  return Array.from(map, ([city, schools]) => ({ city, schools }));
})();
